<script lang="ts" setup>
interface Props {
  options: Option[]
}
interface Option {
  key: string
  value: string
}
const model = defineModel<string>({ required: true })
const props = defineProps<Props>()
</script>

<template>
  <div class="space-x-4">
    <label v-for="option in props.options" :key="option.key">
      <input v-model="model" type="radio" :value="option.value" />
      {{ option.key }}
    </label>
  </div>
</template>
